/*******************************************************************************
 *******************************************************************************
                        GLOBAL FUNCTION DEFINITIONS
 *******************************************************************************
 ******************************************************************************/
void openDoor(void)
{
    MplxLed_On(LED_TWO);
    Timer_AddCallback(&accessTimeOut, OPEN_TIME, true);
}

void accessTimeOut(void)
{
    MplxLed_Off(LED_TWO);
    emitEvent(TIMER_ACCESS_EV);
}

